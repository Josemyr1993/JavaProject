package DTO;

public class EventoDTO {
	private String temaEvento;
	private String localEvento;
	private String nomePalestrante;
	private int numMaxParticipante;
	private String tipoEvento;
	private String dataEvento;
	private String descricaoEvento;
	
	public String getTemaEvento() {
		return temaEvento;
	}
	public void setTemaEvento(String temaEvento) {
		this.temaEvento = temaEvento;
	}
	public String getLocalEvento() {
		return localEvento;
	}
	public void setLocalEvento(String localEvento) {
		this.localEvento = localEvento;
	}
	public String getNomePalestrante() {
		return nomePalestrante;
	}
	public void setNomePalestrante(String nomePalestrante) {
		this.nomePalestrante = nomePalestrante;
	}
	public int getNumMaxParticipante() {
		return numMaxParticipante;
	}
	public void setNumMaxParticipante(int numMaxParticipante) {
		this.numMaxParticipante = numMaxParticipante;
	}
	public String getTipoEvento() {
		return tipoEvento;
	}
	public void setTipoEvento(String tipoEvento) {
		this.tipoEvento = tipoEvento;
	}
	public String getDataEvento() {
		return dataEvento;
	}
	public void setDataEvento(String dataEvento) {
		this.dataEvento = dataEvento;
	}
	public String getDescricaoEvento() {
		return descricaoEvento;
	}
	public void setDescricaoEvento(String descricaoEvento) {
		this.descricaoEvento = descricaoEvento;
	}
}
